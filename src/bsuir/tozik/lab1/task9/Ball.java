package bsuir.tozik.lab1.task9;

public class Ball {
    private double weight;
    private String colour;

    public Ball(double weight, String colour) {
        this.weight = weight;
        this.colour = colour;
    }

    public String getColour() {
        return colour;
    }

    public double getWeight() {
        return weight;
    }
}
